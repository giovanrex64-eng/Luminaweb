/**
 * Obtiene el lista de ciudades para una provincia
 * @param {string} provinceName - Nombre de la provincia
 * @returns {Array} Lista de ciudades con formato { name, postal }
 */
export const getCiudadesPorProvincia = (provinceName) => {
  return CIUDADES_POR_PROVINCIA[provinceName] || [];
};

/**
 * Obtiene el código postal de una ciudad en una provincia
 * @param {string} provinceName - Nombre de la provincia
 * @param {string} cityName - Nombre de la ciudad
 * @returns {string} Código postal o vacío si no encuentra
 */
export const getPostalCode = (provinceName, cityName) => {
  const ciudades = getCiudadesPorProvincia(provinceName);
  const ciudad = ciudades.find(
    c => c.name.toLowerCase() === cityName.toLowerCase()
  );
  return ciudad ? ciudad.postal : '';
};

/**
 * Obtiene lista de provincias con ciudades disponibles
 * @returns {Array} Lista de nombres de provincias
 */
export const getProvinciasConCiudades = () => {
  return Object.keys(CIUDADES_POR_PROVINCIA);
};
